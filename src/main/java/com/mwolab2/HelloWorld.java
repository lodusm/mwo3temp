public class HelloWorld {
    
    double average(double a, double b){
        return (a+b)/2;
    }

    boolean isEven(int a){
        if (a%2==0) return true;
        else return false;
    }

    boolean isNotEven(int a){
        if (a%2==1) return true;
        else return false;
    }
}